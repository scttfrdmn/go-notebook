// Cache policy — the best eviction rule depends on the workload, and here's the proof.
//
// You have a cache that holds N items and a stream of accesses that wants more than N.
// When it's full and a miss comes in, something has to go — and the rule you pick for
// *what goes* is the eviction policy. The two classics:
//
//   - **LRU** (least-recently-used) evicts whatever hasn't been touched in the longest
//     time. It bets on **recency**: what you used lately, you'll use again.
//   - **LFU** (least-frequently-used) evicts whatever has been touched the fewest
//     times. It bets on **popularity**: what's been hot stays hot.
//
// "Which is better?" is the wrong question — it has no answer without the workload,
// and this notebook exists to make that concrete. Flip the **workload** toggle:
//
//   - a **working-set** stream (locality — a moving window of hot keys) is a recency
//     workload, and **LRU wins at every cache size**. Watch its hit-rate curve: it
//     *cliffs* upward the moment the cache is big enough to hold the working set, then
//     goes flat — more cache buys nothing once the set fits. That plateau is the
//     answer to "is 2× the cache worth it?": past the cliff, no.
//   - a **Zipf** stream (a few globally-popular keys dominate) is a popularity
//     workload, and **LFU wins at every cache size** — it keeps the hot keys that LRU
//     keeps aging out.
//
// Same two policies, opposite winners, decided entirely by the shape of the traffic.
// The chart plots hit-rate against cache size for BOTH policies at once, so you see
// them cross (or not) and find the knee.
//
// The mechanism: each policy is run over the **whole access stream to a fixed
// horizon inside one cell** — a pure function of (workload, size, stream length).
// The stream is generated by a seeded LCG, so it's byte-identical across a policy
// comparison and reproducible under a scrub; the hit-rates come from actually
// replaying it, not a formula. Same fixed-horizon-pure shape as the PID and n-body
// notebooks. WASM-live.
//
// DEFERRED, honestly (the taxi pattern): #109 framed this over a 100M-line production
// trace streamed out-of-core via a Rel[Access] handle. That out-of-core delivery is a
// separate claim from the teaching, and it lives with taxi/queue in the batch bucket.
// Here the stream is an in-memory synthetic one; everything about the policy lesson is
// real, the scale is not what's being shown.

package cachepolicy

import (
	"fmt"
	"strconv"
	"strings"
)

const (
	streamLen = 6000 // accesses replayed per policy (the fixed horizon)
	universe  = 300  // distinct keys the stream can reference
)

// ---------------------------------------------------------------------------
// Inputs
// ---------------------------------------------------------------------------

// Workload: 0 = working-set (locality — a moving window of hot keys, a recency
// workload), 1 = Zipf (a few globally-popular keys dominate, a popularity workload).
// This is the toggle that flips which policy wins.
//
//notebook:slider min=0 max=1 step=1
func workloadPick() (workload int) { return 0 }

// Cache size — items held before eviction kicks in. The marker line on the chart; the
// curves are swept across all sizes, this picks the one the verdict reports.
//
//notebook:slider min=5 max=200 step=5
func cacheSize() (size int) { return 50 }

// Working-set size — how many keys are "hot" at once in the working-set workload (the
// window that drifts through the key space). LRU's hit-rate cliffs when the cache
// crosses this. Ignored by the Zipf workload.
//
//notebook:slider min=10 max=120 step=5
func workingSetSize() (ws int) { return 50 }

// ---------------------------------------------------------------------------
// Compute (Go) — generate the stream, replay both policies across all sizes.
// ---------------------------------------------------------------------------

// stream is the deterministic access trace for the chosen workload: `streamLen`
// references from a fixed-seed LCG, so it's byte-identical every recompute and across
// a policy comparison (that identity is the experiment — only the policy differs).
// Pure in (workload, ws).
func stream(workload int, ws int) (trace Trace) {
	refs := make([]int, streamLen)
	var s uint32 = 0x00ABCDEF // fixed seed — no clock, no math/rand global
	rnd := func(mod int) int {
		s = s*1664525 + 1013904223
		if mod <= 0 {
			return 0
		}
		return int(s>>8) % mod
	}
	if workload == 0 {
		// working set: strong locality within a window that drifts slowly, so LRU has
		// a sharp cliff when the cache can finally hold the window.
		base := 0
		for i := range refs {
			if rnd(60) == 0 {
				base = (base + ws/3) % universe // window drifts occasionally
			}
			refs[i] = (base + rnd(ws)) % universe
		}
	} else {
		// Zipf-ish: bias toward low-numbered keys (min of several draws), so a handful
		// of keys are globally hot — popularity, which rewards LFU.
		for i := range refs {
			v := rnd(universe)
			for k := 0; k < 4; k++ {
				if w := rnd(universe); w < v {
					v = w
				}
			}
			refs[i] = v
		}
	}
	return Trace{Refs: refs, Workload: workload}
}

// curves replays BOTH policies over the whole stream at every cache size, returning
// the two hit-rate curves. Pure in (trace): the hit-rate at each size comes from
// actually running the eviction rule over the references, never a closed form — so the
// crossing point and the cliff are earned, not assumed.
func curves(trace Trace) (result Curves) {
	sizes := make([]int, 0, universe/5)
	for c := 5; c <= universe; c += 5 {
		sizes = append(sizes, c)
	}
	lru := make([]float64, len(sizes))
	lfu := make([]float64, len(sizes))
	for i, c := range sizes {
		lru[i] = hitRateLRU(trace.Refs, c)
		lfu[i] = hitRateLFU(trace.Refs, c)
	}
	return Curves{Sizes: sizes, LRU: lru, LFU: lfu, Workload: trace.Workload}
}

// ---------------------------------------------------------------------------
// Views
// ---------------------------------------------------------------------------

// The hit-rate curves: LRU and LFU plotted against cache size for the chosen workload,
// with a marker at the selected size. Watch them cross (or not) — the winner is a
// property of the workload, and the knee is the answer to "is more cache worth it?".
//
//notebook:height=400
func rateChart(result Curves, size int) (chart Chart) {
	return Chart{Curves: result, Marker: size}
}

// The verdict at the selected cache size: each policy's hit-rate and which one wins on
// this workload. Flip the workload toggle and watch the winner change.
func verdict(result Curves, size int, workload int) (report Readout) {
	lru, lfu := at(result.Sizes, result.LRU, size), at(result.Sizes, result.LFU, size)
	winner := "tie"
	switch {
	case lru > lfu+0.005:
		winner = "LRU (recency)"
	case lfu > lru+0.005:
		winner = "LFU (popularity)"
	}
	wl := "working-set (locality)"
	if workload == 1 {
		wl = "Zipf (popularity)"
	}
	return Readout{Cards: []Card{
		{Label: "workload", Value: wl, Caption: "the traffic shape decides everything"},
		{Label: "LRU hit-rate", Value: pct(lru), Caption: "keeps what you touched recently"},
		{Label: "LFU hit-rate", Value: pct(lfu), Caption: "keeps what you touch often"},
		{Label: "winner @ size " + strconv.Itoa(size), Value: winner, Caption: "flip the workload — the winner flips too"},
	}}
}

