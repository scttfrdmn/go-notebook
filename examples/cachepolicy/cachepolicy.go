// ===========================================================================
// Policies — replayed over the reference stream, hit-rate returned.
// ===========================================================================

// hitRateLRU replays the stream through an LRU cache of the given capacity and returns
// the fraction of accesses that hit. Recency order is an MRU-at-end slice.
func hitRateLRU(refs []int, capacity int) float64 {
	if capacity <= 0 {
		return 0
	}
	order := make([]int, 0, capacity) // LRU at front, MRU at end
	in := make(map[int]bool, capacity)
	hits := 0
	for _, k := range refs {
		if in[k] {
			hits++
			for i, x := range order {
				if x == k {
					order = append(order[:i], order[i+1:]...)
					break
				}
			}
			order = append(order, k)
			continue
		}
		if len(order) >= capacity {
			evict := order[0]
			order = order[1:]
			delete(in, evict)
		}
		order = append(order, k)
		in[k] = true
	}
	return float64(hits) / float64(len(refs))
}

// hitRateLFU replays the stream through an LFU cache: on eviction, drop the resident
// key with the lowest access count. (Ties break by lowest key id — deterministic.)
func hitRateLFU(refs []int, capacity int) float64 {
	if capacity <= 0 {
		return 0
	}
	freq := make(map[int]int, capacity)
	in := make(map[int]bool, capacity)
	hits := 0
	for _, k := range refs {
		freq[k]++ // count every reference, resident or not
		if in[k] {
			hits++
			continue
		}
		if len(in) >= capacity {
			evictK, evictF := -1, 1<<30
			for x := range in {
				if freq[x] < evictF || (freq[x] == evictF && x < evictK) {
					evictF = freq[x]
					evictK = x
				}
			}
			delete(in, evictK)
		}
		in[k] = true
	}
	return float64(hits) / float64(len(refs))
}

// ===========================================================================
// Helpers
// ===========================================================================

func pct(v float64) string { return strconv.FormatFloat(v*100, 'f', 1, 64) + "%" }

// at returns the y value whose size entry equals (or is nearest below) the target — so
// the verdict reads the same swept curve the chart draws, not a re-run.
func at(sizes []int, ys []float64, target int) float64 {
	best, bestY := -1, 0.0
	for i, s := range sizes {
		if s <= target && s > best {
			best = s
			bestY = ys[i]
		}
	}
	if best < 0 && len(ys) > 0 {
		return ys[0]
	}
	return bestY
}

func esc(s string) string {
	r := strings.NewReplacer("&", "&amp;", "<", "&lt;", ">", "&gt;", `"`, "&quot;")
	return r.Replace(s)
}

