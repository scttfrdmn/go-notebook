func f1(v float64) string { return strconv.FormatFloat(v, 'f', 1, 64) }

func esc(s string) string {
	r := strings.NewReplacer("&", "&amp;", "<", "&lt;", ">", "&gt;", `"`, "&quot;")
	return r.Replace(s)
}

// ===========================================================================
// Types
// ===========================================================================

// Job is one unit of work: it arrives at a tick, needs Width nodes for Dur ticks.
type Job struct {
	ID, Arrive, Width, Dur int
}

// Queue is the generated job set plus the pool size it competes for.
type Queue struct {
	Jobs  []Job
	Nodes int
}

// Plan is a placed schedule: the start tick of every job (by ID) plus wait metrics.
type Plan struct {
	Jobs       []Job
	Nodes      int
	Start      []int
	MedianWait float64
	MeanWait   float64
	MaxWait    int
	Makespan   int
}

// Gantt draws the placed schedule as bars on a node×time grid.
type Gantt struct{ Plan Plan }

func (g Gantt) Render() Rendered {
	p := g.Plan
	const w, h, pad = 720.0, 440.0, 40.0
	span := p.Makespan
	if span < 1 {
		span = 1
	}
	plotW, plotH := w-2*pad, h-2*pad
	sx := func(t int) float64 { return pad + float64(t)/float64(span)*plotW }
	// vertical: stack node-rows; a job of Width w occupies w adjacent rows.
	rowH := plotH / float64(p.Nodes)

	var b strings.Builder
	fmt.Fprintf(&b, `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 %.0f %.0f">`, w, h)
	fmt.Fprintf(&b, `<rect width="%.0f" height="%.0f" fill="#fff"/>`, w, h)
	fmt.Fprintf(&b, `<rect x="%.0f" y="%.0f" width="%.0f" height="%.0f" fill="#e7ebf0" stroke="#e7ebf0"/>`,
		pad, pad, plotW, plotH)

	// node gridlines
	for n := 0; n <= p.Nodes; n++ {
		y := pad + float64(n)*rowH
		fmt.Fprintf(&b, `<line x1="%.1f" y1="%.1f" x2="%.1f" y2="%.1f" stroke="#e7ebf0"/>`, pad, y, w-pad, y)
	}

	// Place bars greedily onto free node-rows over each job's [start, start+dur). We
	// re-derive row occupancy here purely for drawing (the schedule already guaranteed
	// capacity); this just assigns each job to specific rows so bars don't overlap.
	rowFree := make([][]bool, p.Nodes)
	for r := range rowFree {
		rowFree[r] = make([]bool, span+1)
		for t := range rowFree[r] {
			rowFree[r][t] = true
		}
	}
	// draw in start order for stable, readable stacking.
	order := append([]Job(nil), p.Jobs...)
	sort.Slice(order, func(a, bb int) bool {
		if p.Start[order[a].ID] != p.Start[order[bb].ID] {
			return p.Start[order[a].ID] < p.Start[order[bb].ID]
		}
		return order[a].ID < order[bb].ID
	})
	for _, j := range order {
		st := p.Start[j.ID]
		// find the lowest run of j.Width contiguous rows free across the job's span.
		base := -1
		for r := 0; r+j.Width <= p.Nodes; r++ {
			ok := true
			for rr := r; rr < r+j.Width && ok; rr++ {
				for t := st; t < st+j.Dur && t <= span; t++ {
					if !rowFree[rr][t] {
						ok = false
						break
					}
				}
			}
			if ok {
				base = r
				break
			}
		}
		if base < 0 {
			base = 0 // shouldn't happen; capacity was guaranteed
		}
		for rr := base; rr < base+j.Width; rr++ {
			for t := st; t < st+j.Dur && t <= span; t++ {
				rowFree[rr][t] = false
			}
		}
		x0, x1 := sx(st), sx(st+j.Dur)
		y0 := pad + float64(base)*rowH
		bh := float64(j.Width) * rowH
		// color: wide (blocking) jobs stand out; narrow jobs muted.
		fill := "#2a78d6"
		if j.Width >= p.Nodes {
			fill = "#d03b3b" // full-width blocker
		} else if float64(j.Width) >= float64(p.Nodes)*0.6 {
			fill = "#fab219" // wide
		}
		fmt.Fprintf(&b, `<rect x="%.1f" y="%.1f" width="%.1f" height="%.1f" rx="2" fill=%q stroke="#fff" stroke-width="0.7"/>`,
			x0, y0+1, x1-x0, bh-2, fill)
	}

	fmt.Fprintf(&b, `<text x="%.0f" y="24" font-family="sans-serif" font-size="12" fill="#1b3a6b">Gantt — jobs on the node×time grid (height = nodes held, red = full-width blocker)</text>`, pad)
	fmt.Fprintf(&b, `<text x="%.1f" y="%.1f" font-family="sans-serif" font-size="10" fill="#5b6472">time →</text>`, w-pad-46, h-pad+22)
	fmt.Fprintf(&b, `<text x="%.1f" y="%.1f" font-family="sans-serif" font-size="10" fill="#5b6472" transform="rotate(-90 %.1f %.1f)">nodes</text>`,
		float64(14), pad+plotH/2, float64(14), pad+plotH/2)
	b.WriteString(`</svg>`)
	return Rendered{MIME: "image/svg+xml", Data: b.String()}
}

type Card struct{ Label, Value, Caption string }
type Readout struct{ Cards []Card }

// Render draws the stats as a row of labeled cards. A composite value with no Render
// method carries no MIME and the client leaves the cell hidden — so the cards render
// themselves. Engine-called, not a cell, so fmt here is fine.
func (r Readout) Render() Rendered {
	var b strings.Builder
	b.WriteString(`<div style="display:flex;gap:16px;flex-wrap:wrap">`)
	for _, c := range r.Cards {
		b.WriteString(`<div style="flex:1;min-width:160px;border:1px solid #e7ebf0;border-radius:8px;padding:12px 14px">`)
		fmt.Fprintf(&b, `<div style="font-size:12px;color:#5b6472">%s</div>`, esc(c.Label))
		fmt.Fprintf(&b, `<div style="font-size:24px;font-weight:700;color:#1b3a6b;margin:2px 0">%s</div>`, esc(c.Value))
		if c.Caption != "" {
			fmt.Fprintf(&b, `<div style="font-size:11px;color:#5b6472">%s</div>`, esc(c.Caption))
		}
		b.WriteString(`</div>`)
	}
	b.WriteString(`</div>`)
	return Rendered{MIME: "text/html", Data: b.String()}
}

type Rendered struct{ MIME, Data string }

type Markdown string

func (m Markdown) Render() Rendered { return Rendered{"text/markdown", string(m)} }
