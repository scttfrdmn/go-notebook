// The Gantt chart: one bar per job on a node×time grid — x is when it runs, height is
// the nodes it holds, color marks the widest (blocking) jobs. Read it like `squeue`:
// under FIFO, idle whitespace stacks behind a wide bar nothing may pass; under
// backfill, small bars flow into that whitespace. Same jobs, different packing.
//
//notebook:height=440
func gantt(plan Plan) (chart Gantt) {
	return Gantt{Plan: plan}
}

// The verdict: the wait-time numbers the policy trades on. Median wait is the headline
// — flip the policy and watch it move on an unchanged job set.
func stats(plan Plan) (report Readout) {
	return Readout{Cards: []Card{
		{Label: "median wait", Value: f1(plan.MedianWait) + " ticks", Caption: "the headline — halved by backfill on the same jobs"},
		{Label: "mean wait", Value: f1(plan.MeanWait) + " ticks", Caption: "the long tail behind a blocked queue"},
		{Label: "makespan", Value: strconv.Itoa(plan.Makespan) + " ticks", Caption: "when the last job finishes — tighter packing, sooner"},
	}}
}

