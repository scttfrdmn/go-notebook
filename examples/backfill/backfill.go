//go:notebook
//
// Backfill — the scheduler trick that cuts wait without cutting the line.
//
// A batch scheduler (Slurm, on every HPC cluster) has a queue of jobs, each needing
// some number of nodes for some wall-time, and a fixed pool of nodes to place them on.
// The order it runs them in is *policy*, and the policy you pick changes how long
// everyone waits — on the exact same jobs and the exact same hardware.
//
// The naive policy is **FIFO**: run jobs in arrival order, and if the one at the head
// of the queue doesn't fit yet (it wants more nodes than are free), *everyone waits
// behind it.* A single wide job that can't start blocks a hundred one-node jobs that
// could have — the nodes sit idle, and the queue backs up behind a hole nothing is
// allowed to fill. Every cluster user has watched this happen.
//
// **Backfill** (specifically EASY backfill, the Slurm default) is the fix, and it's
// cleverer than "just reorder": it reserves a start time for the head job so it isn't
// starved, then lets *later* jobs jump ahead into the idle gaps — but only if they
// fit *and finish in time not to delay that reservation.* Small jobs stream into the
// holes a wide job is waiting on; the wide job still starts exactly when it would
// have. Nobody at the front pays for it. That's the property that makes it safe to
// turn on, and the one this notebook makes you watch:
//
//   **the head job starts at the same tick under both policies — backfill only fills
//   the gaps around it.** Median wait drops; the blocking job is not sacrificed.
//
// Drag the load (how many jobs), the pool (how many nodes), and toggle the policy.
// Read the **Gantt chart** the way you read `squeue`: each bar is a job, x is time, its
// height is the nodes it holds. Under FIFO you'll see idle whitespace stacked up
// behind a wide bar; flip to backfill and small bars flow into that whitespace — the
// same jobs, packed.
//
// The mechanism: the schedule is a **pure function of (jobs, nodes, policy)** — place
// every job on a node×time grid and read back its start tick. No Prev[T], no clock,
// no RNG at runtime (the job set is generated by a seeded LCG, so it's byte-identical
// across a policy flip — that identity is the whole experiment). Change the policy and
// the entire schedule recomputes from scratch; scrub freely. WASM-live.

package backfill

import (
	"fmt"
	"sort"
	"strconv"
	"strings"
)

// horizon: the node×time grid is this many ticks wide. Comfortably past any schedule
// the sliders can produce, so nothing spills off the end.
const horizon = 4000

// ---------------------------------------------------------------------------
// Inputs
// ---------------------------------------------------------------------------

// Number of jobs in the queue. More jobs → more contention → the policy matters more.
//
//notebook:slider min=6 max=48 step=1
func jobCount() (jobs int) { return 24 }

// Nodes in the pool. The whole fleet every job competes for; a job wanting more nodes
// than are free has to wait.
//
//notebook:slider min=4 max=16 step=1
func poolNodes() (nodes int) { return 8 }

// Policy: 0 = FIFO (strict arrival order — one wide job blocks the queue behind it),
// 1 = EASY backfill (let small jobs fill the gaps without delaying the head job).
//
//notebook:slider min=0 max=1 step=1
func policyPick() (policy int) { return 1 }

// ---------------------------------------------------------------------------
// Compute (Go) — generate the jobs, then place them. All pure.
// ---------------------------------------------------------------------------

// queue is the deterministic job set: `jobs` jobs from a fixed-seed LCG, so it is
// byte-identical every recompute and — crucially — identical across a policy flip.
// That identity is the experiment: the ONLY thing that changes between the two Gantt
// charts is the placement, never the work. Pure in (jobs, nodes).
func queue(jobs int, nodes int) (work Queue) {
	var s uint32 = 0x01234567 // fixed seed — no clock, no math/rand global
	rnd := func(mod int) int {
		s = s*1664525 + 1013904223
		return int(s>>8) % mod
	}
	js := make([]Job, jobs)
	at := 0
	for i := range js {
		at += rnd(3) // arrivals cluster: 0–2 ticks apart
		w := 1 + rnd(nodes)
		if rnd(4) == 0 {
			w = nodes // ~1 in 4 is a full-width blocker — the jobs that cause the jam
		}
		js[i] = Job{ID: i, Arrive: at, Width: w, Dur: 3 + rnd(12)}
	}
	return Queue{Jobs: js, Nodes: nodes}
}

// schedule places every job on the node×time grid under the chosen policy and returns
// the start tick of each, plus the wait-time metrics. Pure in (work, policy): a job's
// start is read off where it lands on the grid, so waits are REAL (start − arrival),
// never assumed. FIFO gates the queue in arrival order (head-of-line blocking); EASY
// backfill reserves the head's earliest slot and lets later jobs that fit sooner run
// ahead — never past the head's reservation.
func schedule(work Queue, policy int) (plan Plan) {
	nodes := work.Nodes
	free := make([]int, horizon)
	for t := range free {
		free[t] = nodes
	}
	start := make([]int, len(work.Jobs))

	// canFit / earliest / place operate on the running free-grid.
	canFit := func(j Job, at int) bool {
		for t := at; t < at+j.Dur && t < horizon; t++ {
			if free[t] < j.Width {
				return false
			}
		}
		return true
	}
	earliest := func(j Job) int {
		for at := j.Arrive; at < horizon; at++ {
			if canFit(j, at) {
				return at
			}
		}
		return horizon - 1
	}
	place := func(j Job, at int) {
		for t := at; t < at+j.Dur && t < horizon; t++ {
			free[t] -= j.Width
		}
		start[j.ID] = at
	}

	// queue order: by arrival, ties by ID (stable, deterministic).
	order := make([]Job, len(work.Jobs))
	copy(order, work.Jobs)
	sort.Slice(order, func(a, b int) bool {
		if order[a].Arrive != order[b].Arrive {
			return order[a].Arrive < order[b].Arrive
		}
		return order[a].ID < order[b].ID
	})

	if policy == 0 {
		// FIFO with head-of-line blocking: a job may not start before any job ahead
		// of it in the queue. `gate` is the running max of placed starts — the queue
		// can't overtake its own head, so a wide job that waits stalls everyone.
		gate := 0
		for _, j := range order {
			at := j.Arrive
			if at < gate {
				at = gate
			}
			for !canFit(j, at) {
				at++
			}
			place(j, at)
			if at > gate {
				gate = at
			}
		}
	} else {
		// EASY backfill: repeatedly take the head, reserve its earliest slot, then let
		// any remaining job whose own earliest slot is no later than the head's run
		// ahead. Because a backfilled job fits at a tick ≤ the head's start, it never
		// pushes the head's reservation out — the safety guarantee.
		pending := order
		for len(pending) > 0 {
			head := pending[0]
			hAt := earliest(head)
			place(head, hAt)
			var still []Job
			for _, j := range pending[1:] {
				if earliest(j) <= hAt {
					place(j, earliest(j))
				} else {
					still = append(still, j)
				}
			}
			pending = still
		}
	}

	waits := make([]int, len(work.Jobs))
	makespan := 0
	for _, j := range work.Jobs {
		waits[j.ID] = start[j.ID] - j.Arrive
		if end := start[j.ID] + j.Dur; end > makespan {
			makespan = end
		}
	}
	return Plan{
		Jobs: work.Jobs, Nodes: nodes, Start: start,
		MedianWait: medianInt(waits),
		MeanWait:   meanInt(waits),
		MaxWait:    maxInt(waits),
		Makespan:   makespan,
	}
}

