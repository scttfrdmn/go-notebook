// Package gen turns a notebook [graph.Graph] into compilable Go source: a
// registry the engine can execute, generated into the notebook's own package
// so it can see unexported cells.
//
// The generated code is deliberately dumb. It emits exactly two things:
//
//   - a registry of [engine.Node] values — one closure per cell that calls the
//     real cell function with type-asserted inputs and packs its named results
//     into engine.Outputs. The assertions are safe by construction because
//     codegen knew the static types from the graph.
//   - flattened per-cell metadata (label, directives).
//
// It knows nothing about widgets, renderers, purity policy, or the scheduler.
// Anything more would be the framework growing back inside the code generator,
// where it would go unnoticed. If codegen ever needs to understand presentation
// or reactivity, that is a design smell, not a feature.
package gen

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
	"strings"

	"github.com/scttfrdmn/go-notebook/internal/analyze"
	"github.com/scttfrdmn/go-notebook/internal/graph"
)

// GeneratedFile is one synthesized Go source file: its virtual path relative to
// the module (where it must appear to live for the build) and its formatted
// content. Codegen writes nothing to disk — the caller maps these into a
// go build -overlay (see internal/gen/overlay.go).
type GeneratedFile struct {
	// Name is the base filename, e.g. "notebook_gen.go".
	Name string
	// Content is the gofmt'd source.
	Content []byte
}

// Registry generates the notebook_gen.go registry file for the notebook
// package described by info and graph g.
//
// Cells that cannot run this milestone are omitted from the executable registry
// but still reported: any cell with a Delayed (Prev[T]) parameter is a fold,
// which the engine does not yet support. Omitting it keeps the generated code
// compilable; the analyzer has already emitted an "unsupported" diagnostic.
func Registry(g *graph.Graph, info analyze.PackageInfo) (GeneratedFile, error) {
	var b bytes.Buffer

	fmt.Fprintf(&b, "// Code generated by notebook; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", info.Name)
	fmt.Fprintf(&b, "import (\n\t%q\n\n\t%q\n)\n\n",
		"context", "github.com/scttfrdmn/go-notebook/engine")

	writeGenCellType(&b)

	// Emit cells in source order for stable, readable output.
	fmt.Fprintf(&b, "// NotebookCells is the generated cell registry.\n")
	fmt.Fprintf(&b, "var NotebookCells = []engine.Node{\n")
	for _, id := range g.Order {
		c := g.Cells[id]
		if isRunnable(c) {
			writeCell(&b, c)
		}
	}
	fmt.Fprintf(&b, "}\n\n")

	writeMeta(&b, g)

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		// Return the unformatted source in the error so failures are debuggable;
		// a format failure means codegen produced invalid Go, which is a bug.
		return GeneratedFile{}, fmt.Errorf("formatting generated registry: %w\n---\n%s", err, b.String())
	}
	return GeneratedFile{Name: "notebook_gen.go", Content: formatted}, nil
}

// writeGenCellType emits the genCell adapter: a static shape plus a closure,
// implementing engine.Node.
func writeGenCellType(b *bytes.Buffer) {
	b.WriteString(`// genCell adapts a cell function to engine.Node: its static shape plus a
// closure that type-asserts inputs and packs named results.
type genCell struct {
	id   engine.CellID
	in   []engine.Symbol
	out  []engine.Symbol
	pure bool
	fn   func(ctx context.Context, in engine.Inputs) (engine.Outputs, error)
}

func (c genCell) ID() engine.CellID       { return c.id }
func (c genCell) In() []engine.Symbol     { return c.in }
func (c genCell) Out() []engine.Symbol    { return c.out }
func (c genCell) Pure() bool              { return c.pure }
func (c genCell) Run(ctx context.Context, in engine.Inputs) (engine.Outputs, error) {
	return c.fn(ctx, in)
}

`)
}

// writeCell emits one genCell literal for a runnable cell.
func writeCell(b *bytes.Buffer, c *graph.Cell) {
	fmt.Fprintf(b, "\tgenCell{\n")
	fmt.Fprintf(b, "\t\tid:   %q,\n", c.ID)
	fmt.Fprintf(b, "\t\tin:   []engine.Symbol{%s},\n", symbolList(wiredInputs(c)))
	fmt.Fprintf(b, "\t\tout:  []engine.Symbol{%s},\n", symbolList(dataOutputs(c)))
	fmt.Fprintf(b, "\t\tpure: %t,\n", c.Pure)
	fmt.Fprintf(b, "\t\tfn: func(ctx context.Context, in engine.Inputs) (engine.Outputs, error) {\n")
	writeCellBody(b, c)
	fmt.Fprintf(b, "\t\t},\n")
	fmt.Fprintf(b, "\t},\n")
}

// writeCellBody emits the call to the real cell function and the packing of its
// results into engine.Outputs. Result names drive both the call's assignment
// targets and the Outputs keys — the named result IS the edge.
func writeCellBody(b *bytes.Buffer, c *graph.Cell) {
	args := callArgs(c)
	results := resultNameStrings(c)
	hasErr := cellHasError(c)

	// Build the assignment target list: named results, then err if present.
	var targets []string
	targets = append(targets, results...)
	if hasErr {
		targets = append(targets, "err")
	}

	call := fmt.Sprintf("%s(%s)", c.ID, strings.Join(args, ", "))
	if len(targets) == 0 {
		// Shouldn't happen (a cell has >=1 named result), but stay safe.
		fmt.Fprintf(b, "\t\t\t_ = %s\n\t\t\treturn engine.Outputs{}, nil\n", call)
		return
	}

	fmt.Fprintf(b, "\t\t\t%s := %s\n", strings.Join(targets, ", "), call)
	if hasErr {
		fmt.Fprintf(b, "\t\t\tif err != nil {\n\t\t\t\treturn nil, err\n\t\t\t}\n")
	}
	fmt.Fprintf(b, "\t\t\treturn engine.Outputs{")
	for i, r := range results {
		if i > 0 {
			b.WriteString(", ")
		}
		fmt.Fprintf(b, "%q: %s", r, r)
	}
	fmt.Fprintf(b, "}, nil\n")
}

// callArgs renders the argument expressions for the cell function call, in
// signature order: a context param becomes ctx; a wired param becomes a
// type-asserted lookup into the inputs map.
func callArgs(c *graph.Cell) []string {
	var args []string
	for _, p := range c.Params {
		switch p.Kind {
		case graph.Injected:
			args = append(args, "ctx")
		case graph.Wired:
			args = append(args, fmt.Sprintf("in[%q].(%s)", p.Name, p.Type))
		case graph.Delayed:
			// Unreachable: cells with Delayed params are not runnable and are
			// filtered out before codegen. Guard anyway.
			args = append(args, "nil /* delayed: unsupported */")
		}
	}
	return args
}

// writeMeta emits the flattened per-cell presentation metadata.
func writeMeta(b *bytes.Buffer, g *graph.Graph) {
	fmt.Fprintf(b, "// NotebookMeta is the flattened per-cell presentation metadata.\n")
	fmt.Fprintf(b, "var NotebookMeta = []engine.CellMeta{\n")
	for _, id := range g.Order {
		c := g.Cells[id]
		fmt.Fprintf(b, "\t{ID: %q, Leaf: %q, Label: %q, Directives: %s},\n",
			c.ID, leafSymbol(c), c.Label, directiveLiteral(c.Directives))
	}
	fmt.Fprintf(b, "}\n")
}

// leafSymbol returns the symbol a control cell produces (its first named data
// result), or "" if the cell is not a control. This is the symbol the head, UI,
// and --set address — a leaf is identified by what it produces, not its name.
func leafSymbol(c *graph.Cell) graph.Symbol {
	if _, ok := c.Directives["slider"]; !ok {
		return ""
	}
	for _, r := range c.Results {
		if !r.IsError && r.Name != "" {
			return r.Name
		}
	}
	return ""
}

// directiveLiteral renders a directive map as a Go composite literal with keys
// in sorted order (stable output), or "nil" when empty.
func directiveLiteral(d map[string]string) string {
	if len(d) == 0 {
		return "nil"
	}
	keys := make([]string, 0, len(d))
	for k := range d {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	var parts []string
	for _, k := range keys {
		parts = append(parts, fmt.Sprintf("%q: %q", k, d[k]))
	}
	return "map[string]string{" + strings.Join(parts, ", ") + "}"
}

// symbolList renders symbols as a comma-separated list of quoted engine.Symbol
// literals (the []engine.Symbol{...} wrapper is written by the caller).
func symbolList(syms []graph.Symbol) string {
	var parts []string
	for _, s := range syms {
		parts = append(parts, fmt.Sprintf("%q", s))
	}
	return strings.Join(parts, ", ")
}

// isRunnable reports whether a cell can be executed this milestone. A cell with
// a Delayed (Prev[T]) parameter is a fold, which the engine does not yet
// support, so it is omitted from the executable registry.
func isRunnable(c *graph.Cell) bool {
	for _, p := range c.Params {
		if p.Kind == graph.Delayed {
			return false
		}
	}
	return true
}

// wiredInputs returns the cell's wired parameter symbols (the edges it
// consumes), excluding injected and delayed params.
func wiredInputs(c *graph.Cell) []graph.Symbol {
	var out []graph.Symbol
	for _, p := range c.Params {
		if p.Kind == graph.Wired {
			out = append(out, p.Name)
		}
	}
	return out
}

// dataOutputs returns the cell's data result symbols (excluding a trailing
// error and any unnamed result).
func dataOutputs(c *graph.Cell) []graph.Symbol {
	var out []graph.Symbol
	for _, r := range c.Results {
		if r.IsError || r.Name == "" {
			continue
		}
		out = append(out, r.Name)
	}
	return out
}

// resultNameStrings returns the cell's data result names as strings, for use
// as Go identifiers (assignment targets) and Outputs keys.
func resultNameStrings(c *graph.Cell) []string {
	var out []string
	for _, s := range dataOutputs(c) {
		out = append(out, string(s))
	}
	return out
}

// cellHasError reports whether the cell has a trailing error result.
func cellHasError(c *graph.Cell) bool {
	for _, r := range c.Results {
		if r.IsError {
			return true
		}
	}
	return false
}
