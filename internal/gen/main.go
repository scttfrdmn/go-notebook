// MainPackage generates the tiny main package that imports the notebook
// package's registry, builds the engine runtime, and serves it — or, with
// --headless, runs one wave and prints results.
//
// The generated main stays free of topology knowledge (the engine computes
// levels). It does, however, generate a TYPE-AWARE --set parser: codegen knows
// each leaf's static Go type from the graph, so `--set servers=120` parses the
// string into the leaf's real type (int, a named float64, etc.) rather than
// guessing at runtime. That is codegen doing what only it can — it has the
// static types — without knowing anything about presentation or reactivity.
func MainPackage(g *graph.Graph, info analyze.PackageInfo) (GeneratedFile, error) {
	const alias = "nb"
	var b bytes.Buffer

	var body bytes.Buffer
	writeLeaves(&body, alias)
	writeSetLeafValue(&body, g, alias)
	body.WriteString(mainBody(alias))
	bodyStr := body.String()

	fmt.Fprintf(&b, "// Code generated by notebook; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package main\n\n")
	fmt.Fprintf(&b, "import (\n")
	// Every import here is used by the fixed bootstrap regardless of the leaf set:
	// the --set/--json machinery references all of them. (The old string-parser
	// pulled in strconv only for numeric-leaf notebooks; routing --set through
	// engine.CoerceWire removed the one conditional import.)
	for _, imp := range []string{"context", "encoding/json", "flag", "fmt", "log", "os", "sort", "strings"} {
		fmt.Fprintf(&b, "\t%q\n", imp)
	}
	fmt.Fprintf(&b, "\n\t%q\n\t%q\n\t%s %q\n)\n\n",
		"github.com/scttfrdmn/go-notebook/engine",
		"github.com/scttfrdmn/go-notebook/engine/server",
		alias, info.ImportPath)
	b.WriteString(bodyStr)

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return GeneratedFile{}, fmt.Errorf("formatting generated main: %w\n---\n%s", err, b.String())
	}
	return GeneratedFile{Name: "main.go", Content: formatted}, nil
}

// WASMMainPackage generates the browser entry point: the SAME registry, engine,
// scheduler, and head as the server main, driven over the syscall/js boundary
// (engine/wasm) instead of an HTTP/SSE server. The only difference from
// [MainPackage] is the transport — the whole point of the WASM topology test.
//
// It reuses the type-aware leaf coercer (setLeafValue): browser edits arrive as
// JS values (float64/bool/string), the same shapes the SSE /set path coerces,
// so downstream type assertions stay safe. Carries a //go:build js && wasm tag
// so it only compiles for the browser target.
func WASMMainPackage(g *graph.Graph, info analyze.PackageInfo) (GeneratedFile, error) {
	const alias = "nb"
	var b bytes.Buffer

	var body bytes.Buffer
	writeLeaves(&body, alias)
	writeSetLeafValue(&body, g, alias)
	body.WriteString(wasmMainBody(alias))
	bodyStr := body.String()

	fmt.Fprintf(&b, "//go:build js && wasm\n\n")
	fmt.Fprintf(&b, "// Code generated by notebook; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package main\n\n")
	fmt.Fprintf(&b, "import (\n")
	// context/fmt/log are all referenced by the fixed browser bootstrap and the
	// one leaf coercer (setLeafValue's fmt.Errorf, the set closure's log.Printf),
	// regardless of the leaf set — so they are unconditional, like the native main.
	for _, imp := range []string{"context", "fmt", "log"} {
		fmt.Fprintf(&b, "\t%q\n", imp)
	}
	fmt.Fprintf(&b, "\n\t%q\n\t%q\n\t%s %q\n)\n\n",
		"github.com/scttfrdmn/go-notebook/engine",
		"github.com/scttfrdmn/go-notebook/engine/wasm",
		alias, info.ImportPath)
	b.WriteString(bodyStr)

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return GeneratedFile{}, fmt.Errorf("formatting generated wasm main: %w\n---\n%s", err, b.String())
	}
	return GeneratedFile{Name: "main_wasm.go", Content: formatted}, nil
}

// wasmMainBody is the fixed browser bootstrap: build the runtime and hand it to
// the wasm transport, which subscribes to events, runs the initial wave, and
// installs the JS-callable set function. No flags, no server — the browser is
// the host.
func wasmMainBody(alias string) string {
	return `func main() {
	rt := engine.NewRuntime(engine.Config{
		Nodes:  ` + alias + `.NotebookCells,
		Leaves: leaves(),
	}, engine.NewHead(), engine.NewMemoStore())

	set := func(ctx context.Context, r *engine.Runtime, leaf string, raw any) {
		if err := setLeafValue(ctx, r, leaf, raw); err != nil {
			log.Printf("notebook: set %q: %v", leaf, err)
		}
	}
	wasm.RunNotebook(rt, ` + alias + `.NotebookMeta, ` + alias + `.NotebookProvenance, ` + alias + `.NotebookLayout, set)
}
`
}

// writeLeaves emits leaves(): the symbols the user can edit (cells carrying a
// control directive). Deriving them from metadata keeps codegen's registry pass
// unaware of presentation.
func writeLeaves(b *bytes.Buffer, alias string) {
	fmt.Fprintf(b, `// leaves are the symbols the user can edit: the Leaf symbol of each control
// cell. A leaf is identified by the symbol it produces, not the cell's name.
func leaves() []engine.LeafID {
	var out []engine.LeafID
	for _, m := range %s.NotebookMeta {
		if m.Leaf != "" {
			out = append(out, engine.LeafID(m.Leaf))
		}
	}
	return out
}

`, alias)
}

// writeSetLeafValue emits setLeafValue(): the ONE leaf coercer, shared by every
// write path — the SSE /set handler, the wasm notebook.set bridge, and the CLI
// --set flag. JSON numbers arrive as float64; this converts each leaf's raw
// value to its static Go type before writing through the head, so downstream
// cells' type assertions are safe.
//
// It returns an error rather than logging-and-continuing, so a caller decides
// the consequence: the live transports log it (a bad drag must not kill the
// process); the CLI makes it fatal (a bad --set is a user error worth stopping
// for). Every failure surfaces — an unknown leaf, an uncoercible selection, or
// a scalar that doesn't match its leaf's kind — because a silent store of the
// wrong shape is exactly the inert-control bug this path exists to prevent.
func writeSetLeafValue(b *bytes.Buffer, g *graph.Graph, alias string) {
	b.WriteString(`// setLeafValue coerces a raw leaf value to its static type and writes it through
// rt.Set (head chokepoint + version bump + wave). It is the blocking form used by
// the CLI --set and the wasm bridge; the HTTP /set path uses coerceLeafValue to
// validate synchronously and runs rt.Set itself (so the POST need not wait for
// the wave). Both share the one coercer, so the wire contract cannot fork.
func setLeafValue(ctx context.Context, rt *engine.Runtime, name string, raw any) error {
	typed, err := coerceLeafValue(name, raw)
	if err != nil {
		return err
	}
	rt.Set(ctx, engine.LeafID(name), typed)
	return nil
}

// coerceLeafValue coerces a raw leaf value to its static Go type WITHOUT writing
// it — the pure, synchronous half of a leaf edit. raw is normalized first via
// engine.CoerceWire: json.Number → float64 and []any → []string/[]float64, so
// both the scalar cases (which assert float64) and widget leaves see clean
// primitives. It returns the typed value ready for rt.Set, or an error on any
// mismatch: engine.ErrUnknownLeaf for a name no cell produces, engine.ErrBadValue
// for a value that will not coerce — so a caller (the HTTP handler) can map each
// to the right status. No path stores a raw value it could not coerce.
func coerceLeafValue(name string, raw any) (any, error) {
	norm, ok := engine.CoerceWire(raw)
	if !ok {
		return nil, fmt.Errorf("%w: leaf %q: cannot coerce selection %v (%T)", engine.ErrBadValue, name, raw, raw)
	}
	raw = norm
	switch name {
`)
	for _, leaf := range leafSymbols(g) {
		fmt.Fprintf(b, "\tcase %q:\n", leaf.name)
		writeCoerceCase(b, alias, leaf)
	}
	b.WriteString(`	default:
		return nil, fmt.Errorf("%w: %s", engine.ErrUnknownLeaf, name)
	}
}

`)
}

// writeCoerceCase converts a raw JSON leaf value to its static type and RETURNS
// it (the caller writes it via rt.Set). JSON numbers arrive as float64 and bools
// as bool; the leaf's underlying kind (from the IR, not guessed) selects the
// conversion. A scalar leaf handed a value of the wrong kind returns
// engine.ErrBadValue rather than dropping the set: a --set that names a numeric
// leaf but passes a string is a user error worth reporting, not a silent no-op.
func writeCoerceCase(b *bytes.Buffer, alias string, leaf leafInfo) {
	name := string(leaf.name)
	qualified := qualifyType(alias, leaf.typ)
	switch underlyingKind(leaf.underlying) {
	case kindInt:
		fmt.Fprintf(b, `		f, ok := raw.(float64)
		if !ok {
			return nil, fmt.Errorf("%%w: leaf %%q: want a number, got %%v (%%T)", engine.ErrBadValue, %q, raw, raw)
		}
		return %s(int(f)), nil
`, name, qualified)
	case kindFloat:
		fmt.Fprintf(b, `		f, ok := raw.(float64)
		if !ok {
			return nil, fmt.Errorf("%%w: leaf %%q: want a number, got %%v (%%T)", engine.ErrBadValue, %q, raw, raw)
		}
		return %s(f), nil
`, name, qualified)
	case kindBool:
		fmt.Fprintf(b, `		v, ok := raw.(bool)
		if !ok {
			return nil, fmt.Errorf("%%w: leaf %%q: want a bool, got %%v (%%T)", engine.ErrBadValue, %q, raw, raw)
		}
		return %s(v), nil
`, name, qualified)
	default:
		// A widget leaf: raw is already homogenized (CoerceWire at entry) to
		// []string / []float64 / string / bool — the clean primitive the
		// notebook's Reconcile asserts. Return it; Reconcile does the domain
		// resolution (label→Theme) itself. A cell never sees a wire shape.
		fmt.Fprintf(b, `		return raw, nil
`)
	}
}

// mainBody is the fixed engine bootstrap: open head, apply --set overrides,
// build runtime (serial if asked), then serve or run-once-and-print.
func mainBody(alias string) string {
	return `func main() {
	addr := flag.String("addr", "127.0.0.1:8080", "listen address")
	headless := flag.Bool("headless", false, "run once and exit, no server")
	asJSON := flag.Bool("json", false, "print final cell values as JSON (implies --headless)")
	serial := flag.Bool("serial", false, "disable goroutine fan-out (per-cell stdout)")
	headPath := flag.String("head", "notebook-head.json", "head persistence file")
	var sets multiFlag
	flag.Var(&sets, "set", "override a leaf: --set leaf=value (repeatable)")
	flag.Parse()

	// headSpecified reports whether --head was passed explicitly, distinct from
	// the flag merely holding its default. It selects head persistence by mode
	// below.
	headSpecified := false
	flag.Visit(func(f *flag.Flag) {
		if f.Name == "head" {
			headSpecified = true
		}
	})

	// Choose the head by mode. A batch run (--headless/--json) must be a pure
	// function of (source, --set): it starts from a fresh in-memory head and
	// applies only the --set overrides, so the SAME binary and flags give the
	// SAME output regardless of what a prior interactive run left in the working
	// directory. (An implicit notebook-head.json in the CWD silently merging into
	// batch output is exactly the reproducibility hole #208 — printJSON's own
	// provenance claim demands determinism.) An interactive run keeps its
	// persisted head so restart is a non-event. Passing --head explicitly opts
	// batch back into a named, reproducible-by-path head — the deliberate case.
	var head *engine.Head
	if (*headless || *asJSON) && !headSpecified {
		head = engine.NewHead()
	} else {
		var err error
		head, err = engine.OpenHead(*headPath)
		if err != nil {
			log.Fatalf("open head: %v", err)
		}
	}

	rt := engine.NewRuntime(engine.Config{
		Nodes:  ` + alias + `.NotebookCells,
		Leaves: leaves(),
		Serial: *serial,
	}, head, engine.NewMemoStore())

	ctx := context.Background()

	// Apply --set overrides through the SAME coercer the live transports use:
	// parse the value string as JSON (so 120, 3.5, true, "x", [1,2] all decode),
	// then setLeafValue coerces to the leaf's static type and writes via rt.Set —
	// the documented chokepoint. This deletes the old string-only parser that
	// could set only int/float/bool and silently stored a raw string for a
	// composite leaf. A bad --set is fatal: a user override is worth stopping for.
	for _, s := range sets {
		name, raw, ok := cutEquals(s)
		if !ok {
			log.Fatalf("bad --set %q: want leaf=value", s)
		}
		if err := setLeafValue(ctx, rt, name, parseSetValue(raw)); err != nil {
			log.Fatalf("--set: %v", err)
		}
	}

	if *headless || *asJSON {
		rt.RunAll(ctx)
		if *asJSON {
			printJSON(` + alias + `.NotebookProvenance, rt.Finals())
		}
		return
	}

	set := func(c context.Context, leaf string, raw any) error {
		// Coerce synchronously so /set can answer 404/422 for a bad edit; on
		// success apply the write and run the wave in the background so the POST
		// returns without waiting for recompute (results stream over /events).
		typed, err := coerceLeafValue(leaf, raw)
		if err != nil {
			log.Printf("notebook: /set: %v", err)
			return err
		}
		go rt.Set(c, engine.LeafID(leaf), typed)
		return nil
	}
	// onReady fires once the listener is bound, with the RESOLVED address (so
	// --addr :0 reports its real port). It emits one machine-readable readiness
	// line on stdout — the notebook-as-service contract (docs/notebook-as-service.md):
	// a launcher reads {event:"ready",addr,provenance} instead of polling-and-hoping,
	// and the human-facing log line still prints for interactive use.
	onReady := func(resolved string) {
		log.Printf("notebook serving on http://%s", resolved)
		ready := map[string]any{
			"event":      "ready",
			"addr":       resolved,
			"provenance": ` + alias + `.NotebookProvenance,
		}
		if b, err := json.Marshal(ready); err == nil {
			fmt.Println(string(b))
		}
	}
	if err := server.ServeNotebookReady(ctx, *addr, rt, ` + alias + `.NotebookMeta, ` + alias + `.NotebookProvenance, ` + alias + `.NotebookLayout, set, onReady); err != nil {
		log.Printf("server: %v", err)
		os.Exit(1)
	}
}

// printJSON writes a self-identifying batch result: the provenance (what
// produced this — source hash, commit) alongside the final cell values. An
// sbatch result that names the notebook and the exact source/commit that
// produced it is the reproducibility claim with teeth. The values live under a
// "values" key so a cell can never collide with an identity field, and each
// value is encoded independently — a value that does not marshal (a float
// Inf/NaN, which JSON cannot represent) becomes its fmt string, so batch output
// never fails wholesale on one exotic value (an unstable queue legitimately
// produces an infinite wait).
func printJSON(prov engine.Provenance, finals map[engine.Symbol]any) {
	keys := make([]string, 0, len(finals))
	for k := range finals {
		keys = append(keys, string(k))
	}
	sort.Strings(keys)

	provJSON, _ := json.Marshal(prov)
	os.Stdout.WriteString("{\n")
	fmt.Fprintf(os.Stdout, "  \"provenance\": %s,\n", provJSON)
	os.Stdout.WriteString("  \"values\": {\n")
	for i, k := range keys {
		enc, err := json.Marshal(finals[engine.Symbol(k)])
		if err != nil {
			enc, _ = json.Marshal(fmt.Sprint(finals[engine.Symbol(k)]))
		}
		key, _ := json.Marshal(k)
		comma := ","
		if i == len(keys)-1 {
			comma = ""
		}
		fmt.Fprintf(os.Stdout, "    %s: %s%s\n", key, enc, comma)
	}
	os.Stdout.WriteString("  }\n}\n")
}

// multiFlag collects repeated --set flags.
type multiFlag []string

func (m *multiFlag) String() string { return "" }
func (m *multiFlag) Set(v string) error {
	*m = append(*m, v)
	return nil
}

// parseSetValue turns a --set value string into the decoded JSON value the leaf
// coercer expects, so the CLI and the browser feed setLeafValue the same shapes.
// A value that parses as JSON (120, 3.5, true, ["a","b"], [1,2]) decodes to its
// natural Go value; anything else (a bare word like "hpc7i", meant as a string)
// is taken verbatim as a string. json.Number keeps large ints exact until the
// coercer narrows to the leaf's type. This is the one place a CLI string becomes
// a wire value; the coercion to a static Go type happens in setLeafValue.
func parseSetValue(raw string) any {
	dec := json.NewDecoder(strings.NewReader(raw))
	dec.UseNumber()
	var v any
	if err := dec.Decode(&v); err != nil {
		return raw // not JSON: a bare string leaf value
	}
	// Trailing junk after a valid JSON token (e.g. "1 2") means it wasn't a
	// single scalar the user intended; treat the whole thing as a string.
	if dec.More() {
		return raw
	}
	return v
}

// cutEquals splits "leaf=value" on the first '='.
func cutEquals(s string) (name, val string, ok bool) {
	for i := 0; i < len(s); i++ {
		if s[i] == '=' {
			return s[:i], s[i+1:], true
		}
	}
	return "", "", false
}
`
}

// --- codegen helpers for leaf types ---

type leafInfo struct {
	name       graph.Symbol
	typ        string
	underlying string // the result's basic kind from the IR ("int"/"float64"/"bool"/…)
}

// leafSymbols returns the leaves and their result types, sorted by name for
// stable output. Leaf-ness is the analyzer's type-driven graph.Cell.IsLeaf, not
// a directive; a leaf's single data result gives the type.
func leafSymbols(g *graph.Graph) []leafInfo {
	var out []leafInfo
	for _, id := range g.Order {
		c := g.Cells[id]
		if !c.IsLeaf {
			continue
		}
		// Same leaf edge selection as leafSymbol/leafTypeLiteral (firstDataResult),
		// so the coercer this feeds addresses exactly the result a client's Type
		// describes — one selection rule, three readers.
		if r := firstDataResult(c); r != nil {
			out = append(out, leafInfo{name: r.Name, typ: r.Type, underlying: r.Underlying})
		}
	}
	sort.Slice(out, func(i, j int) bool { return out[i].name < out[j].name })
	return out
}

type typeKind int

const (
	kindOther typeKind = iota
	kindInt
	kindFloat
	kindBool
)

// underlyingKind classifies a leaf by its underlying basic kind, taken from the
// IR (the analyzer resolved it via go/types — even through a named type like
// PerHour over float64). No string-guessing: a name we don't recognize is
// kindOther and falls back to a raw set.
func underlyingKind(underlying string) typeKind {
	switch underlying {
	case "int", "int8", "int16", "int32", "int64",
		"uint", "uint8", "uint16", "uint32", "uint64", "uintptr":
		return kindInt
	case "float32", "float64":
		return kindFloat
	case "bool":
		return kindBool
	default:
		return kindOther
	}
}

// qualifyType prefixes a notebook-package type with the import alias so the
// generated main can name it. Builtins are left bare.
func qualifyType(alias, typ string) string {
	switch typ {
	case "int", "int8", "int16", "int32", "int64",
		"uint", "uint8", "uint16", "uint32", "uint64",
		"float32", "float64", "string", "bool":
		return typ
	}
	if strings.ContainsAny(typ, ".[]*") {
		return typ // qualified or composite; leave as-is
	}
	return alias + "." + typ
}
