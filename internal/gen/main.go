// MainPackage generates the tiny main package that imports the notebook
// package's registry, builds the engine runtime, and serves it — or, with
// --headless, runs one wave and prints results.
//
// The generated main stays free of topology knowledge (the engine computes
// levels). It does, however, generate a TYPE-AWARE --set parser: codegen knows
// each leaf's static Go type from the graph, so `--set servers=120` parses the
// string into the leaf's real type (int, a named float64, etc.) rather than
// guessing at runtime. That is codegen doing what only it can — it has the
// static types — without knowing anything about presentation or reactivity.
func MainPackage(g *graph.Graph, info analyze.PackageInfo) (GeneratedFile, error) {
	const alias = "nb"
	var b bytes.Buffer

	// Build the body first, then emit only the imports it actually references.
	// A notebook with no numeric-leaf controls (e.g. lego, which has only a
	// brush) generates no strconv-using case, so strconv must not be imported —
	// an unused import is a compile error. Deriving imports from the body avoids
	// this whole class of bug.
	var body bytes.Buffer
	writeLeaves(&body, alias)
	writeSetLeaf(&body, g, alias)
	writeSetLeafValue(&body, g, alias)
	body.WriteString(mainBody(alias))
	bodyStr := body.String()

	fmt.Fprintf(&b, "// Code generated by notebook; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package main\n\n")
	fmt.Fprintf(&b, "import (\n")
	// Always used by the fixed bootstrap.
	for _, imp := range []string{"context", "encoding/json", "flag", "fmt", "log", "os", "sort"} {
		fmt.Fprintf(&b, "\t%q\n", imp)
	}
	// Conditional: only when a generated case references it.
	if strings.Contains(bodyStr, "strconv.") {
		fmt.Fprintf(&b, "\t%q\n", "strconv")
	}
	fmt.Fprintf(&b, "\n\t%q\n\t%q\n\t%s %q\n)\n\n",
		"github.com/scttfrdmn/go-notebook/engine",
		"github.com/scttfrdmn/go-notebook/engine/server",
		alias, info.ImportPath)
	b.WriteString(bodyStr)

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return GeneratedFile{}, fmt.Errorf("formatting generated main: %w\n---\n%s", err, b.String())
	}
	return GeneratedFile{Name: "main.go", Content: formatted}, nil
}

// WASMMainPackage generates the browser entry point: the SAME registry, engine,
// scheduler, and head as the server main, driven over the syscall/js boundary
// (engine/wasm) instead of an HTTP/SSE server. The only difference from
// [MainPackage] is the transport — the whole point of the WASM topology test.
//
// It reuses the type-aware leaf coercer (setLeafValue): browser edits arrive as
// JS values (float64/bool/string), the same shapes the SSE /set path coerces,
// so downstream type assertions stay safe. Carries a //go:build js && wasm tag
// so it only compiles for the browser target.
func WASMMainPackage(g *graph.Graph, info analyze.PackageInfo) (GeneratedFile, error) {
	const alias = "nb"
	var b bytes.Buffer

	var body bytes.Buffer
	writeLeaves(&body, alias)
	writeSetLeafValue(&body, g, alias)
	body.WriteString(wasmMainBody(alias))
	bodyStr := body.String()

	fmt.Fprintf(&b, "//go:build js && wasm\n\n")
	fmt.Fprintf(&b, "// Code generated by notebook; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package main\n\n")
	fmt.Fprintf(&b, "import (\n\t%q\n", "context")
	if strings.Contains(bodyStr, "strconv.") {
		fmt.Fprintf(&b, "\t%q\n", "strconv")
	}
	fmt.Fprintf(&b, "\n\t%q\n\t%q\n\t%s %q\n)\n\n",
		"github.com/scttfrdmn/go-notebook/engine",
		"github.com/scttfrdmn/go-notebook/engine/wasm",
		alias, info.ImportPath)
	b.WriteString(bodyStr)

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return GeneratedFile{}, fmt.Errorf("formatting generated wasm main: %w\n---\n%s", err, b.String())
	}
	return GeneratedFile{Name: "main_wasm.go", Content: formatted}, nil
}

// wasmMainBody is the fixed browser bootstrap: build the runtime and hand it to
// the wasm transport, which subscribes to events, runs the initial wave, and
// installs the JS-callable set function. No flags, no server — the browser is
// the host.
func wasmMainBody(alias string) string {
	return `func main() {
	rt := engine.NewRuntime(engine.Config{
		Nodes:  ` + alias + `.NotebookCells,
		Leaves: leaves(),
	}, engine.NewHead(), engine.NewMemoStore())

	set := func(ctx context.Context, r *engine.Runtime, leaf string, raw any) {
		setLeafValue(ctx, r, leaf, raw)
	}
	wasm.RunNotebook(rt, ` + alias + `.NotebookMeta, ` + alias + `.NotebookProvenance, set)
}
`
}

// writeLeaves emits leaves(): the symbols the user can edit (cells carrying a
// control directive). Deriving them from metadata keeps codegen's registry pass
// unaware of presentation.
func writeLeaves(b *bytes.Buffer, alias string) {
	fmt.Fprintf(b, `// leaves are the symbols the user can edit: the Leaf symbol of each control
// cell. A leaf is identified by the symbol it produces, not the cell's name.
func leaves() []engine.LeafID {
	var out []engine.LeafID
	for _, m := range %s.NotebookMeta {
		if m.Leaf != "" {
			out = append(out, engine.LeafID(m.Leaf))
		}
	}
	return out
}

`, alias)
}

// writeSetLeaf emits setLeaf(): a type-aware parser for --set leaf=value. Each
// leaf gets a case converting the string to its static Go type. This is the
// one place codegen's knowledge of static types earns its keep at the CLI.
func writeSetLeaf(b *bytes.Buffer, g *graph.Graph, alias string) {
	b.WriteString(`// setLeaf parses a --set value string into the leaf's static type and writes
// it through the head's single Set chokepoint.
func setLeaf(head *engine.Head, name, raw string) error {
	switch name {
`)
	for _, leaf := range leafSymbols(g) {
		fmt.Fprintf(b, "\tcase %q:\n", leaf.name)
		writeParseCase(b, alias, leaf)
	}
	b.WriteString(`	default:
		return errUnknownLeaf(name)
	}
}

func errUnknownLeaf(name string) error {
	return &leafError{name}
}

type leafError struct{ name string }

func (e *leafError) Error() string { return "unknown leaf: " + e.name }

`)
}

// writeSetLeafValue emits setLeafValue(): the server-side coercer. JSON numbers
// arrive as float64; this converts each leaf's raw value to its static Go type
// before writing through the head, so downstream cells' type assertions are
// safe. It mirrors setLeaf (the CLI's string parser) for the JSON-value path.
func writeSetLeafValue(b *bytes.Buffer, g *graph.Graph, alias string) {
	b.WriteString(`// setLeafValue coerces a raw JSON leaf value to its static type and writes it
// through rt.Set (head chokepoint + version bump + wave).
func setLeafValue(ctx context.Context, rt *engine.Runtime, name string, raw any) {
	switch name {
`)
	for _, leaf := range leafSymbols(g) {
		fmt.Fprintf(b, "\tcase %q:\n", leaf.name)
		writeCoerceCase(b, alias, leaf)
	}
	b.WriteString(`	}
}

`)
}

// writeCoerceCase converts a raw JSON leaf value to its static type and sets it
// via rt.Set. JSON numbers arrive as float64 and bools as bool; the leaf's
// underlying kind (from the IR, not guessed) selects the conversion.
func writeCoerceCase(b *bytes.Buffer, alias string, leaf leafInfo) {
	name := string(leaf.name)
	qualified := qualifyType(alias, leaf.typ)
	switch underlyingKind(leaf.underlying) {
	case kindInt:
		fmt.Fprintf(b, `		if f, ok := raw.(float64); ok {
			rt.Set(ctx, %q, %s(int(f)))
		}
		return
`, name, qualified)
	case kindFloat:
		fmt.Fprintf(b, `		if f, ok := raw.(float64); ok {
			rt.Set(ctx, %q, %s(f))
		}
		return
`, name, qualified)
	case kindBool:
		fmt.Fprintf(b, `		if v, ok := raw.(bool); ok {
			rt.Set(ctx, %q, %s(v))
		}
		return
`, name, qualified)
	default:
		fmt.Fprintf(b, `		rt.Set(ctx, %q, raw)
		return
`, name)
	}
}

// writeParseCase emits the body of a setLeaf case (--set leaf=value from the
// CLI), converting the string to the leaf's static type via its underlying
// kind so the cell's runtime assertion is safe.
func writeParseCase(b *bytes.Buffer, alias string, leaf leafInfo) {
	name := string(leaf.name)
	qualified := qualifyType(alias, leaf.typ)
	switch underlyingKind(leaf.underlying) {
	case kindInt:
		fmt.Fprintf(b, `		v, err := strconv.Atoi(raw)
		if err != nil {
			return err
		}
		head.Set(%q, %s(v))
		return nil
`, name, qualified)
	case kindFloat:
		fmt.Fprintf(b, `		v, err := strconv.ParseFloat(raw, 64)
		if err != nil {
			return err
		}
		head.Set(%q, %s(v))
		return nil
`, name, qualified)
	case kindBool:
		fmt.Fprintf(b, `		v, err := strconv.ParseBool(raw)
		if err != nil {
			return err
		}
		head.Set(%q, %s(v))
		return nil
`, name, qualified)
	default:
		// Fall back to a string set; the cell's assertion will catch a real
		// mismatch. Rare for leaves.
		fmt.Fprintf(b, `		head.Set(%q, raw)
		return nil
`, name)
	}
}

// mainBody is the fixed engine bootstrap: open head, apply --set overrides,
// build runtime (serial if asked), then serve or run-once-and-print.
func mainBody(alias string) string {
	return `func main() {
	addr := flag.String("addr", "127.0.0.1:8080", "listen address")
	headless := flag.Bool("headless", false, "run once and exit, no server")
	asJSON := flag.Bool("json", false, "print final cell values as JSON (implies --headless)")
	serial := flag.Bool("serial", false, "disable goroutine fan-out (per-cell stdout)")
	headPath := flag.String("head", "notebook-head.json", "head persistence file")
	var sets multiFlag
	flag.Var(&sets, "set", "override a leaf: --set leaf=value (repeatable)")
	flag.Parse()

	head, err := engine.OpenHead(*headPath)
	if err != nil {
		log.Fatalf("open head: %v", err)
	}
	for _, s := range sets {
		name, raw, ok := cutEquals(s)
		if !ok {
			log.Fatalf("bad --set %q: want leaf=value", s)
		}
		if err := setLeaf(head, name, raw); err != nil {
			log.Fatalf("--set: %v", err)
		}
	}

	rt := engine.NewRuntime(engine.Config{
		Nodes:  ` + alias + `.NotebookCells,
		Leaves: leaves(),
		Serial: *serial,
	}, head, engine.NewMemoStore())

	ctx := context.Background()
	if *headless || *asJSON {
		rt.RunAll(ctx)
		if *asJSON {
			printJSON(` + alias + `.NotebookProvenance, rt.Finals())
		}
		return
	}

	log.Printf("notebook serving on http://%s", *addr)
	set := func(c context.Context, leaf string, raw any) {
		setLeafValue(c, rt, leaf, raw)
	}
	if err := server.ServeNotebook(ctx, *addr, rt, ` + alias + `.NotebookMeta, ` + alias + `.NotebookProvenance, set); err != nil {
		log.Printf("server: %v", err)
		os.Exit(1)
	}
}

// printJSON writes a self-identifying batch result: the provenance (what
// produced this — source hash, commit) alongside the final cell values. An
// sbatch result that names the notebook and the exact source/commit that
// produced it is the reproducibility claim with teeth. The values live under a
// "values" key so a cell can never collide with an identity field, and each
// value is encoded independently — a value that does not marshal (a float
// Inf/NaN, which JSON cannot represent) becomes its fmt string, so batch output
// never fails wholesale on one exotic value (an unstable queue legitimately
// produces an infinite wait).
func printJSON(prov engine.Provenance, finals map[engine.Symbol]any) {
	keys := make([]string, 0, len(finals))
	for k := range finals {
		keys = append(keys, string(k))
	}
	sort.Strings(keys)

	provJSON, _ := json.Marshal(prov)
	os.Stdout.WriteString("{\n")
	fmt.Fprintf(os.Stdout, "  \"provenance\": %s,\n", provJSON)
	os.Stdout.WriteString("  \"values\": {\n")
	for i, k := range keys {
		enc, err := json.Marshal(finals[engine.Symbol(k)])
		if err != nil {
			enc, _ = json.Marshal(fmt.Sprint(finals[engine.Symbol(k)]))
		}
		key, _ := json.Marshal(k)
		comma := ","
		if i == len(keys)-1 {
			comma = ""
		}
		fmt.Fprintf(os.Stdout, "    %s: %s%s\n", key, enc, comma)
	}
	os.Stdout.WriteString("  }\n}\n")
}

// multiFlag collects repeated --set flags.
type multiFlag []string

func (m *multiFlag) String() string { return "" }
func (m *multiFlag) Set(v string) error {
	*m = append(*m, v)
	return nil
}

// cutEquals splits "leaf=value" on the first '='.
func cutEquals(s string) (name, val string, ok bool) {
	for i := 0; i < len(s); i++ {
		if s[i] == '=' {
			return s[:i], s[i+1:], true
		}
	}
	return "", "", false
}
`
}

// --- codegen helpers for leaf types ---

type leafInfo struct {
	name       graph.Symbol
	typ        string
	underlying string // the result's basic kind from the IR ("int"/"float64"/"bool"/…)
}

// leafSymbols returns the leaves and their result types, sorted by name for
// stable output. Leaf-ness is the analyzer's type-driven graph.Cell.IsLeaf, not
// a directive; a leaf's single data result gives the type.
func leafSymbols(g *graph.Graph) []leafInfo {
	var out []leafInfo
	for _, id := range g.Order {
		c := g.Cells[id]
		if !c.IsLeaf {
			continue
		}
		for _, r := range c.Results {
			if r.IsError || r.Name == "" {
				continue
			}
			out = append(out, leafInfo{name: r.Name, typ: r.Type, underlying: r.Underlying})
			break
		}
	}
	sort.Slice(out, func(i, j int) bool { return out[i].name < out[j].name })
	return out
}

type typeKind int

const (
	kindOther typeKind = iota
	kindInt
	kindFloat
	kindBool
)

// underlyingKind classifies a leaf by its underlying basic kind, taken from the
// IR (the analyzer resolved it via go/types — even through a named type like
// PerHour over float64). No string-guessing: a name we don't recognize is
// kindOther and falls back to a raw set.
func underlyingKind(underlying string) typeKind {
	switch underlying {
	case "int", "int8", "int16", "int32", "int64",
		"uint", "uint8", "uint16", "uint32", "uint64", "uintptr":
		return kindInt
	case "float32", "float64":
		return kindFloat
	case "bool":
		return kindBool
	default:
		return kindOther
	}
}

// qualifyType prefixes a notebook-package type with the import alias so the
// generated main can name it. Builtins are left bare.
func qualifyType(alias, typ string) string {
	switch typ {
	case "int", "int8", "int16", "int32", "int64",
		"uint", "uint8", "uint16", "uint32", "uint64",
		"float32", "float64", "string", "bool":
		return typ
	}
	if strings.ContainsAny(typ, ".[]*") {
		return typ // qualified or composite; leave as-is
	}
	return alias + "." + typ
}
