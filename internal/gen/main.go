package gen

import (
	"bytes"
	"fmt"
	"go/format"

	"github.com/scttfrdmn/go-notebook/internal/analyze"
)

// MainPackage generates the tiny main package that imports the notebook
// package's registry, builds the engine runtime, and serves it.
//
// The generated main stays free of topology knowledge: it hands the registry's
// nodes and the leaf list to engine.NewRuntime, which computes levels itself.
// Leaves are the symbols the user can edit — cells carrying a control directive
// (e.g. //notebook:slider). Deriving them from metadata here (not in codegen's
// registry pass) keeps codegen dumb: it emits data, main wires the engine.
func MainPackage(info analyze.PackageInfo) (GeneratedFile, error) {
	const alias = "nb"
	var b bytes.Buffer

	fmt.Fprintf(&b, "// Code generated by notebook; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package main\n\n")
	fmt.Fprintf(&b, "import (\n")
	fmt.Fprintf(&b, "\t%q\n\t%q\n\t%q\n\t%q\n\n", "context", "flag", "log", "os")
	fmt.Fprintf(&b, "\t%q\n\t%q\n\t%s %q\n)\n\n",
		"github.com/scttfrdmn/go-notebook/engine",
		"github.com/scttfrdmn/go-notebook/engine/server",
		alias, info.ImportPath)

	b.WriteString(mainBody(alias))

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return GeneratedFile{}, fmt.Errorf("formatting generated main: %w\n---\n%s", err, b.String())
	}
	return GeneratedFile{Name: "main.go", Content: formatted}, nil
}

// mainBody is the fixed engine bootstrap. It:
//   - opens the head (persisted, so restart restores the sliders),
//   - derives leaves from cells that carry a control directive,
//   - builds the runtime (levels computed by the engine),
//   - serves, or in --headless mode runs one wave and exits.
func mainBody(alias string) string {
	return `// leaves are the symbols the user can edit: cells with a control directive.
func leaves() []engine.LeafID {
	var out []engine.LeafID
	for _, m := range ` + alias + `.NotebookMeta {
		if _, ok := m.Directives["slider"]; ok {
			out = append(out, engine.LeafID(m.ID))
		}
	}
	return out
}

func main() {
	addr := flag.String("addr", "127.0.0.1:8080", "listen address")
	headless := flag.Bool("headless", false, "run once and exit, no server")
	headPath := flag.String("head", "notebook-head.json", "head persistence file")
	flag.Parse()

	head, err := engine.OpenHead(*headPath)
	if err != nil {
		log.Fatalf("open head: %v", err)
	}
	rt := engine.NewRuntime(engine.Config{
		Nodes:  ` + alias + `.NotebookCells,
		Leaves: leaves(),
	}, head, engine.NewMemoStore())

	ctx := context.Background()
	if *headless {
		rt.RunAll(ctx)
		return
	}

	log.Printf("notebook serving on http://%s", *addr)
	if err := server.Serve(ctx, *addr, rt, ` + alias + `.NotebookMeta); err != nil {
		log.Printf("server: %v", err)
		os.Exit(1)
	}
}
`
}
