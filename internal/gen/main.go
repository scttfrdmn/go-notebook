package gen

import (
	"bytes"
	"fmt"
	"go/format"
	"path"

	"github.com/scttfrdmn/go-notebook/internal/analyze"
)

// MainPackage generates the tiny main package that imports the notebook package
// and drives its registry.
//
// This milestone (M2) exists to produce a build number, so main does the
// minimum that forces the registry to link and be reachable: it imports the
// notebook package and prints the cell/meta counts. M3 replaces this body with
// the engine bootstrap (head + scheduler); the shape — "main imports the
// notebook package's generated registry" — is what M2 pins.
func MainPackage(info analyze.PackageInfo) (GeneratedFile, error) {
	var b bytes.Buffer

	// Import the notebook package under a stable local alias so its package name
	// can be anything without clashing with identifiers here.
	const alias = "nb"
	fmt.Fprintf(&b, "// Code generated by notebook; DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package main\n\n")
	fmt.Fprintf(&b, "import (\n\t%q\n\n\t%s %q\n)\n\n", "fmt", alias, info.ImportPath)
	fmt.Fprintf(&b, "func main() {\n")
	fmt.Fprintf(&b, "\tfmt.Printf(%q, %q, len(%s.NotebookCells), len(%s.NotebookMeta))\n",
		"notebook %s: %d cells, %d meta\n", pkgLabel(info), alias, alias)
	fmt.Fprintf(&b, "}\n")

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return GeneratedFile{}, fmt.Errorf("formatting generated main: %w\n---\n%s", err, b.String())
	}
	return GeneratedFile{Name: "main.go", Content: formatted}, nil
}

// pkgLabel is a human label for the notebook in generated output.
func pkgLabel(info analyze.PackageInfo) string {
	if info.Name != "" {
		return info.Name
	}
	return path.Base(info.ImportPath)
}
